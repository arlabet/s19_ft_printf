#include "ft_printf.h"

int	ft_len_nbr(int nbr)
{
	int i;

	i = 0;
	if (nbr < 0)
		i = 1;
	nbr = ft_abs(nbr);
	while (nbr >= 10)
	{
		nbr = nbr / 10;
		i++;
	}
	return(i + 1);
}